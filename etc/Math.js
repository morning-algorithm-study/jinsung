/* 
 MDN - https://developer.mozilla.org/ko/docs/Web/JavaScript/Reference/Global_Objects/Math/ceil
 Math.ceil() 함수는 주어진 숫자보다 크거나 같은 숫자 중 가장 작은 숫자를 integer 로 반환합니다.
 */
Math.ceil(0.95); // 1
Math.ceil(4); // 4
Math.ceil(7.004); // 8
Math.ceil(-0.95); // -0
Math.ceil(-4); // -4
Math.ceil(-7.004); // -7

/* 
 MDN - https://developer.mozilla.org/ko/docs/Web/JavaScript/Reference/Global_Objects/Math/sqrt
 Math.sqrt() 함수는 숫자의 제곱근을 반환합니다.
 */
Math.sqrt(9); // 3
Math.sqrt(2); // 1.414213562373095
Math.sqrt(1); // 1
Math.sqrt(0); // 0
Math.sqrt(-1); // NaN

/* 
 MDN - https://developer.mozilla.org/ko/docs/Web/JavaScript/Reference/Global_Objects/Math/pow
 Math.pow()함수는base^exponent처럼 base 에 exponent를 제곱한 값을 반환합니다.
 */

console.log(Math.pow(7, 3)); // expected output: 343

console.log(Math.pow(4, 0.5)); // expected output: 2

console.log(Math.pow(7, -2)); // expected output: 0.02040816326530612 //(1/49)

console.log(Math.pow(-7, 0.5)); // expected output: NaN

